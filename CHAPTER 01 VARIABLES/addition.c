#include <stdio.h>
int main()
{
    int a, b;
    printf("Enter the value of a\n");
    scanf("%d", &a);

    printf("Enter the value of b\n");
    scanf("%d", &b);

    printf("the sum of a and b %d\n", a + b);
    printf("the sum of a and b %d\n", a - b);
    printf("the sum of a and b %d\n", a * b);
    printf("the sum of a and b %d\n", a/b);
    printf("the sum of a and b %d\n", a ++);
    printf("the sum of a and b %d\n", ++a);
    printf("the sum of a and b %d\n", a--);
    printf("the sum of a and b %d\n", --a);
    printf("the sum of a and b %d\n", --a);



    return 0;
}